#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned int	i;
	char			*arr;

	i = 0;
	arr = b;
	while (i < len)
	{
		arr[i] = c;
		i++;
	}
	return (b);
}
